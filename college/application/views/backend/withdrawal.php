<script>
    $(document).ready(function () {
        var droplist = $('#Student_studentid');
        droplist.change(function () {
            $('#siblings').empty();
            $.ajax({
                type: "POST",
                url: "viewsiblings",
                data: {studentid: $('#Student_studentid option:selected').val()},
                dataType: "html",
                success: function (data) {
                    $('#siblings').append(data);
                    $('#siblingdetails').show("slow");
                }
            });
        })
    });
    $(document).ready(function () {
        var student = [];
        $('#withdraw').click(function (event) {  //on click 

            if ($('#Student_courseid option:selected').val() == "" && $('#Student_batchid option:selected').val() == "" && $('#Student_studentid option:selected').val() == "") {

                alert("Please select all the fields");
            }
            else {
                $('#promotionstudent tbody tr').each(function (row, tr) {
                    if ($(this).find(":checkbox").prop("checked")) {

                        var studentid = $(tr).find('td:eq(0)').data('id');

                        student.push(studentid);
                    }
                });
            }
            var sendarray = JSON.stringify(student);
            $.ajax({
                type: "POST",
                url: "savepromotiondetails",
                data: {courseid: $('#Student_courseid1 option:selected').val(), batchid: $('#Student_batchid1 option:selected').val(), sendarray: sendarray},
                dataType: "html",
                success: function (data) {
                    alert("Successfully promoted");
                }
            });
        });
    });

    function withdrawal() {
        if ($('#Student_courseid option:selected').val() == "" && $('#Student_batchid option:selected').val() == "" && $('#Student_studentid option:selected').val() == "") {

            alert("Please select all the fields");
        }
        else {
            $.ajax({
                type: "POST",
                url: "withdrawaldetails",
                data: {courseid: $('#Student_courseid option:selected').val(), batchid: $('#Student_batchid option:selected').val(), studentid: $('#Student_studentid option:selected').val()},
                dataType: "html",
                success: function (data) {
                    alert("Success");
                    location.reload();
                }
            });
        }
    }
